X_train = np.column_stack([np.ones(len(train_data)), train_data['x'].values])
y_train = train_data['y'].values.reshape(-1, 1)
X_test = np.column_stack([np.ones(len(test_data)), test_data['x'].values])
y_test = test_data['y'].values.reshape(-1, 1)

def mse(y_true, y_pred):
    return np.mean((y_true - y_pred)**2)

# --- 2. 算法实现 ---

# (1) 最小二乘法 (OLS)
theta_ols = np.linalg.inv(X_train.T @ X_train) @ X_train.T @ y_train

# (2) 梯度下降法 (GD)
theta_gd = np.zeros((2, 1))
alpha = 0.01
for _ in range(1000):
    gradient = (1/len(y_train)) * X_train.T @ (X_train @ theta_gd - y_train)
    theta_gd -= alpha * gradient

# (3) 牛顿法
theta_newton = np.zeros((2, 1))
# 对于线性回归，Hessian = (1/m) * X.T @ X
H = (1/len(y_train)) * X_train.T @ X_train
H_inv = np.linalg.inv(H)
grad = (1/len(y_train)) * X_train.T @ (X_train @ theta_newton - y_train)
theta_newton = theta_newton - H_inv @ grad # 理论上一步到位

# --- 3. 结果打印 ---
methods = [("OLS", theta_ols), ("GD", theta_gd), ("Newton", theta_newton)]
print("--- 线性模型结果 ---")
for name, theta in methods:
    train_err = mse(y_train, X_train @ theta)
    test_err = mse(y_test, X_test @ theta)
    print(f"{name}: 训练误差={train_err:.4f}, 测试误差={test_err:.4f}")

# --- 4. 非线性改进：多项式拟合 ---
def get_poly_features(x, degree):
    return np.column_stack([x**i for i in range(degree + 1)])

